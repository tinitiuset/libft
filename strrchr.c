#include "libft.h"

char	*ft_strrchr(const char *s, int c)
{
	int		i;
	char	*j;

	i = -1;
	j = 0;
	while (s[++i])
	{
		if (s[i] == c)
			j = ((char *)&s[i]);
	}
	return (j);
}
